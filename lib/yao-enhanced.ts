/** 获取指定卦的某一爻的爻辞 */
export function getYaoCi(hexagramName: string, position: number): string {
  const ci = YAO_CI[hexagramName];
  if (!ci || !ci[position - 1]) return "";
  return ci[position - 1];
}

/** 获取卦辞 */
export function getGuaCi(hexagram: Hexagram): string {
  return hexagram.judgment;
}

// ==================== 6. 增强版排盘 ====================

/**
 * 生成增强版六爻排盘细节
 */
export function buildEnhancedLineDetails(
  lines: YaoLine[],
  hexagram: Hexagram,
  dayStem: string,
  topic?: string,
  gender?: string
): {
  lineDetails: YaoLineDetail[];
  nayin: { stems: string[]; branches: string[] };
  sixSpirits: SixSpirit[];
  yongShen: { yongShen: SixRelative; explanation: string };
  world: number;
  response: number;
} {
  const { world, response } = getWorldResponsePosition(lines);
  const nayin = getNayin(hexagram);
  const sixSpirits = getSixSpirits(dayStem);
  const yongShen = getYongShen(topic || "综合", gender);

  // 五行对应
  const trigramElements: Record<TrigramName, FiveElement> = {
    乾: "金", 兑: "金", 离: "火", 震: "木",
    巽: "木", 坎: "水", 艮: "土", 坤: "土"
  };

  // 卦宫五行（用于六亲定位）
  const palaceElement = trigramElements[hexagram.upper] || trigramElements[hexagram.lower];

  const generates: Record<FiveElement, FiveElement> = {
    木: "火", 火: "土", 土: "金", 金: "水", 水: "木"
  };
  const controls: Record<FiveElement, FiveElement> = {
    木: "土", 土: "水", 水: "火", 火: "金", 金: "木"
  };

  function resolveRelative(line: YaoLine): SixRelative {
    const trigram = line.position <= 3 ? hexagram.lower : hexagram.upper;
    const lineElement = trigramElements[trigram];

    if (palaceElement === lineElement) return "兄弟";
    if (generates[palaceElement] === lineElement) return "子孙";
    if (generates[lineElement] === palaceElement) return "父母";
    if (controls[palaceElement] === lineElement) return "妻财";
    return "官鬼";
  }

  const lineDetails: YaoLineDetail[] = lines.map((line) => ({
    position: line.position,
    positionName: positionNames[line.position - 1],
    relation: resolveRelative(line),
    spirit: sixSpirits[line.position - 1],
    worldResponse:
      line.position === world ? "世" : line.position === response ? "应" : null,
    element: trigramElements[line.position <= 3 ? hexagram.lower : hexagram.upper],
    basis: `纳${nayin.stems[line.position - 1] || ""}，六神${sixSpirits[line.position - 1]}，六亲${resolveRelative(line)}。`
  }));

  return {
    lineDetails,
    nayin,
    sixSpirits,
    yongShen,
    world,
    response
  };
}
