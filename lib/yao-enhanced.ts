// ==================== 3. 用神判断 ====================

/**
 * 按占事类型取用神
 *
 * 占事类型 → 用神六亲：
 *   工作/官运 → 官鬼
 *   感情/婚姻（男）→ 妻财（女命看官鬼）
 *   财运 → 妻财
 *   考试/学业 → 父母（文书）
 *   健康/疾病 → 子孙（制鬼之神）
 *   出行 → 驿马 / 父母（交通工具）
 *   合作 → 兄弟
 *   诉讼 → 官鬼
 *   家宅 → 父母
 */
export function getYongShen(topic: string, gender?: string): {
  yongShen: SixRelative;
  explanation: string;
} {
  const t = topic.toLowerCase();

  if (/工作|事业|官运|职位|升职|求职|面试/.test(t)) {
    return { yongShen: "官鬼", explanation: "工作事业以官鬼为用神，官鬼旺相则事业顺遂。" };
  }
  if (/感情|恋爱|婚姻|结婚|复合/.test(t)) {
    if (gender === "女") {
      return { yongShen: "官鬼", explanation: "女命感情以官鬼为用神，代表丈夫或男朋友。" };
    }
    return { yongShen: "妻财", explanation: "男命感情以妻财为用神，代表女朋友或妻子。" };
  }
  if (/财运|投资|收入|生意|赚钱|回款/.test(t)) {
    return { yongShen: "妻财", explanation: "财运以妻财为用神，财星旺则财运佳。" };
  }
  if (/考试|学业|学习|成绩|升学|考证/.test(t)) {
    return { yongShen: "父母", explanation: "考试学业以父母为用神，父母主文书、学业功名。" };
  }
  if (/健康|疾病|看病|身体/.test(t)) {
    return { yongShen: "子孙", explanation: "健康以子孙为用神，子孙能制鬼（病鬼），为安康之兆。" };
  }
  if (/出行|旅行|搬家|外出|行程/.test(t)) {
    return { yongShen: "父母", explanation: "出行以父母为用神，父母主交通工具与行程安排。" };
  }
  if (/合作|合伙|客户|签约/.test(t)) {
    return { yongShen: "兄弟", explanation: "合作以兄弟为用神，兄弟主平等协作关系。" };
  }
  if (/诉讼|官司|纠纷|矛盾|吵架/.test(t)) {
    return { yongShen: "官鬼", explanation: "诉讼以官鬼为用神，官鬼代表官非与裁决。" };
  }
  if (/家宅|房屋|风水|装修/.test(t)) {
    return { yongShen: "父母", explanation: "家宅以父母为用神，父母主房屋、根基。" };
  }

  // 通用 —— 看主要关注点
  return { yongShen: "妻财", explanation: "综合占事，以妻财为用神，兼顾官鬼（事业）与父母（文书）。" };
}

// ==================== 4. 世应规则 ====================

/**
 * 世应定位（京房八宫卦世应规则）
 *
 * 一世卦世在初爻，二世卦世在二爻，三世卦世在三爻，
 * 四世卦世在四爻，五世卦世在五爻，游魂卦世在四爻，
 * 归魂卦世在三爻。应在世隔两位。
 *
 * 简化版：根据变爻位置定位，无动爻则按本宫定位
 */
export function getWorldResponsePosition(lines: YaoLine[]): { world: number; response: number } {
  const movingLines = lines.filter(l => l.isMoving);

  // 有动爻：根据动爻位置定位
  if (movingLines.length >= 0) {
    // 无动爻 → 世在六爻（上爻）
    // 一个动爻 → 世在动爻位
    // 多个动爻 → 世在初爻
    if (movingLines.length === 1) {
      const world = movingLines[0].position;
      const response = ((world + 2) % 6) + 1;
      return { world, response: response as YaoLine["position"] };
    }
    if (movingLines.length === 0) {
      return { world: 6, response: 3 };
    }
    // 多动爻 → 世在初爻
    return { world: 1, response: 4 };
  }

  return { world: 6, response: 3 };
}

